<div data-animation="fadeInUp" class="heading-desc animate-element animate-stagger" style="color:rgba(255,255,255,0.8)">HIV can be transmitted through contact with blood, vaginal and rectal fluids and breast milk from an infected person.  HIV is not found and cannot be spread through urine, sweat or saliva. 

HIV self-tests detect if your body has been previously exposed to HIV. If it has, your body will have produced antibodies specific to HIV to defend itself against the virus.  These antibodies can be detected from oral fluids (as well as in blood). The HIV self-test does not detect the actual virus.   
